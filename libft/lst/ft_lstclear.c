#include "../includes/libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*temp;

	if (lst && del)
	{
		while (*lst)
		{
			temp = (*lst)->next;
			if ((*lst)->content != NULL)
				del((*lst)->content);
			free(*lst);
			*lst = temp;
		}
		*lst = NULL;
	}
}
